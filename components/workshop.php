                    <div style="text-align: justify;font-size: x-large;">
                        This learning session focuses on statistical concepts and enhancing skills in the area of
                        quantitative methods and data analysis. In the first part, topics to be covered include
                        regression analysis and pitfalls or statistical errors prevalent when adjusting for covariates
                        in observational studies. The second part will introduce participants to the concept of
                        measurement error. Participants will be introduced to different types of errors and measurement
                        errors that often occur in observational studies. Some of the topics to be covered include
                        sources of measurement error in public health data and observational studies, types of
                        measurement error, consequences of measurement errors in statistical analysis, and how to adjust
                        and correct for biases due to measurement errors. Examples from nutrition and public health will
                        be provided. This session will be of particular value and use to students/researchers/etc. who
                        are interested in the analysis of observational data collected in public health research.
