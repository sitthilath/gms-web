                            Rectors, deans, administrators, lecturers, delegates, students, and all
                            participants, ladies and gentlemen,
                            I stand before you today with a profound sense of purpose and urgency as we
                            convene at this significant gathering focused on the public health challenges facing
                            the Greater Mekong Subregion (GMS) countries. As the chairperson of the GMS Public Health
                            Academic Network I am deeply honored to address such a distinguished audience comprising
                            scholars, practitioners, policymakers, and advocates dedicated to advancing the health and
                            well-being
                            of our region in the 14th International Public Health Conference among GMS Countries with
                            the
                            theme<b>“Meeting the Sustainable Development Goals through Improved Nutrition, Health
                                Equity,
                                and
                                Climate Change Adaptation.” </b>The Greater Mekong Subregion has a diverse culture,
                            abundant
                            natural
                            resources, and significant economic potential. However, it is also a region dealing with
                            complicated
                            health concerns that necessitate coordinated efforts and novel solutions.<br>
                            The Greater Mekong Subregion (GMS) Public Health Academic Network began in 2008. The
                            Network is dedicated to improving the quality and relevance of educational and training
                            programs in
                            public health in the GMS. The Network is in charge of enhancing collaboration and
                            partnership
                            across the GMS's public health institutions, as well as facilitating collaborative actions
                            among
                            existing networks, policymakers, and researchers interested in health policy and research.
                            The
                            Network also provides a venue for health personnel, academics, and researchers, particularly
                            graduate students and young academic staff from the GMS region, to communicate and exchange
                            scientific and professional expertise in public health from their respective nations.<br>
                            Beyond the immediate threat of COVID-19, we must also address the broader health
                            determinants
                            that disseminate health inequities and hinder sustainable development in our region which
                            have been
                            stated in SDGs. In this regard, I urge all stakeholders gathered here today to prioritize
                            investments in
                            healthcare infrastructure, workforce development, and health promotion initiatives that
                            empower
                            individuals and communities to lead healthier lives. We must adopt a 'health in all
                            policies' approach
                            that integrates health considerations into decision-making across sectors, including
                            education,
                            agriculture, transportation, and urban planning.<br>
                            Furthermore, we must use the power of research, innovation, and technology to drive
                            evidence-based
                            solutions and increase health-care system resilience in the face of new challenges.
                            Collaboration
                            across academics, government agencies, civil society organizations, and the commercial
                            sector is
                            critical for realizing the full potential of scientific advances and turning research
                            discoveries into
                            actual benefits for our communities.<br>
                            As we map the future of public health in the Greater Mekong Subregion, let us remain guided
                            by the
                            principles of equity, solidarity, and sustainability. Together, we have the collective
                            capacity to
                            overcome the most pressing health challenges of our time and build a healthier, more
                            prosperous
                            future for all.
                            Thank you !!


