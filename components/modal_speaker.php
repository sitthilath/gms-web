                        <h1 class="abstract-title fs-4 fw-bold">Abstract</h1>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            Climate change appears to be not only an environmental health problem, but also a nutritional and
                            health issue. Many studies have been conducted to determine the effect of climate change on
                            environmental health, but research on the impact of climate change on nutrition and health is still
                            rare. Therefore, it is important to describe the impact of climate change on weather disasters, food
                            systems, biodiversity loss, nutrient loss, and health.
                        </p>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            Climate change has resulted in weather disasters because of heatwaves, wildfires, drought, floods,
                            and storms. After that, it affected soil fertility and rain patterns, so crop yields and food production
                            dropped. Then the stability of the global food system was attempted and mass food production
                            focused on conventional cereal and crops by using fertilizer and deforestation. As a result,
                            biodiversity and gene diversity of species and ecosystems are lost. Moreover, nutrient content
                            changed or lost, so human health was impacted. The impacts started from increasing insect
                            populations as vectors of disease because of increasing global temperature. Also, the air pollution that
                            comes from wildfire, human activity, and natural disasters could produce many more oxidants which
                            result in lower immune systems and emerging of noncommunicable diseases.
                        </p>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            Hence, Climate change resulted in weather disasters that affected the food system because of
                            decreasing food production, then had an impact on biodiversity loss and nutrition loss. Finally, human
                            health has been attacked by infectious disease and degenerative diseases, so we should maximize one
                            health approach that considers animal health, human health, and the environment.
                        </p>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            <Strong>Keywords:</Strong> biodiversity loss, food systems, vector-borne disease, degenerative disease, weather
                            disasters
                        </p>
