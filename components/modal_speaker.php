                        <h1 class="abstract-title fs-4 fw-bold">Abstract</h1>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            This talk explores equity considerations within public health initiatives in the Greater Mekong Sub-Region, an
                            area characterised by both rapid economic and public health development and persistent health disparities.
                            Challenges include global and regional politics, geographic and cultural diversity, socioeconomic differences,
                            climate/environmental change, and resource and infrastructural limitations. Comparative analysis of existing
                            policies and interventions can identify areas for potential improvement based on principles of social justice,
                            community engagement, and intersectoral collaboration. Highlighting examples and best practices within and
                            beyond the Greater Mekong Sub-Region, we can identify lessons and recommendations for policymakers,
                            practitioners, and other stakeholders on strengthening equity within public health policies and initiatives in the
                            region. The talk advocates for public health equity through considering structural and social health
                            determinants, encouraging public sector engagement with diverse communities, and ensuring equitable health
                            services access in the pursuit of health and wellbeing in the Greater Mekong Sub-Region.
