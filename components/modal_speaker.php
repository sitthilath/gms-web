                        <h1 class="abstract-title fs-4 fw-bold">Abstract</h1>
                        <h1 class="abstract-title fs-4 fw-bold">Global and regional context</h1>
                        <p class="abstract-body fs-5" style="text-align: justify;text-justify: inter-word">
                            While every Member State in the world has made enormous efforts in advancing the SDGs since
                            2015, the 2023 Sustainable Development Goals Report revealed alarming results (1). At the midpoint
                            to the deadline of the 2030 agenda, half of 140 targets which have data available for the evaluation
                            are off track, and 30% of these targets, specifically targets on poverty, hunger and climate, show no
                            progress or regression. The report also underscored inequities, how the COVID-19 pandemic, the
                            triple planetary crisis and the complex geopolitical situation have had disproportionate impacts across
                            the globe and within a country. The result is aligned with the Asia and the Pacific SDG Progress
                            Report 2024 (2). The Asia and the Pacific SDG Progress Report 2024 also alerted the Region with
                            extremely slow progress across all 17 SDGs in the Asia and Pacific Region and urges immediate
                            action for climate change which showed regression below the 2015 baseline. Its sub-analysis for
                            South-East Asia suggests that moderate progress on the SDG 3 Good health and well-being; low
                            progress in the SDG 2 Zero hunger; regression in the SDG 13 Climate action and the SDG 12
                            Responsible consumption and production; and little progress in other SDGs related to climate change.
                        </p>
                        <p class="fs-5 fw-bold">Six Transitions: Investment Pathways to Deliver the SDGs</p>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            In this context, the United Nations proposed six key transitions to accelerate the progress (3): food
                            systems; energy access and affordability; digital connectivity; education; jobs and social protection;
                            and climate change, biodiversity loss and pollution. The SDGs are intertwined, which requires an
                            integrated approach to accelerate the progress across the 17 SDGs. All the key transitions are also
                            interconnected in accelerating the progress of multiple SDGs. The significant impact of climate
                            change on health and nutrition now urges our collective actions to address these challenges in more
                            strategic, innovative and integrated ways (4,5). In the health sector, there are various efforts to build
                            the resilient and sustainable health system to achieve the universal health coverage and other health-
                            related SDGs. Here, a couple of selected innovative approaches in the health sector in Lao PDR will
                            be introduced, using one of the key six transitions, digital transformation.
                        </p>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            <Strong>Actions in the health sector, Lao PDR:</Strong> Data-informed planning and digital transformation to
                            accelerate the progress in achieving the SDGs on health, nutrition and climate change
                        </p>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            In alignment with priorities of the Health Sector Reform Strategy 2021-2030 (6) and the 9th Health
                            Sector Development Plan (7), the Ministry of Health (MOH), Lao PDR has made great efforts to
                            enhance data-informed planning for strategic resource allocation with strong emphasis on vulnerable
                            populations, using the geographic information system. The MOH has mapped all the socio-
                            demographic data of the catchment areas, road access, geographical locations of health facilities,
                            human resources for health, and utilization of healthcare services using the District Health
                            Information Software version 2 into the map of Lao PDR to support the subnational planning for
                            strengthening primary health care. Furthermore, since malnutrition has been identified as a significant
                            public health and development issue in Lao PDR, MOH (8), with support from various development
                            partners, has invested in improving nutrition surveillance system to better understand nutritional
                            trends, identify at-risk populations, and tailor interventions accordingly.
                        </p>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            As part of innovative approaches which the MOH introduced in the health sector, the MOH has also
                            initiated the integration of climate data, such as temperature and rainfall data received from weather
                            stations around the country, into the national Health Management Information System to forecast
                            climate-sensitive diseases using statistical models through an early warning, alert, and response/
                            system (9). Early warning systems can predict and alert authorities of potential health and nutrition
                            risks, such as waterborne diseases, vector-borne diseases, and malnutrition. It also contributes to
                            building the national health system’s and the society’s resilience for future climate-related challenges.
                        </p>

                        <h1 class="abstract-title fs-4 fw-bold">Way forward</h1>
                        <p class="abstract-body fs-5 " style="text-align: justify;text-justify: inter-word">
                            The SDG progress reports at the global and regional levels (1,2) call for urgent actions by the
                            government, development partners, academics, and our communities to address the off-track targets
                            of the SDGs collectively. Regression in achieving the SDG 13, specifically, draws a strong attention
                            of all, considering its significant impact on the SDGs related to health and nutrition and equity issues
                            (10). To address the challenges effectively, we will need to enhance close coordination across
                            relevant sectors beyond health; incorporate innovate tools and solutions; strengthen the health system
                            and the entire society to be more sustainable and resilient in the future; and empower and work with
                            our communities closely to improve our joint ownership and responsibility to achieve the SDGs
                            across the globe.
                        </p>
